public class News {
    private int id;
    private String title;
    private String content;
    private int publishDate;

    public int getId() {
        return id;
    }

    public String getTitle() {
        return title;
    }

    public String getContent() {
        return content;
    }

    public int getPublishDate() {
        return publishDate;
    }
}
